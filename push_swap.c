#include "push_swap.h"

int	main(int argc, char *argv[])
{
	int	i;

	if (argc == 0)
		return (0);
	i = 1;
	while (argv[i] != NULL)
	{
		ft_printf("%s\n", argv[i]);
		i++;
	}
	return (0);
}
